#include "3-calc.h"

/**
 * op_add - function
 * @a: param
 * @b: param
 *
 * Return: 0 success
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function
 * @a: param
 * @b: param
 *
 * Return: 0 success
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function
 * @a: param
 * @b: param
 *
 * Return: 0 success
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function
 * @a: param
 * @b: param
 *
 * Return: 0 success
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function
 * @a: param
 * @b: param
 *
 * Return: 0 success
*/
int op_mod(int a, int b)
{
	return (a % b);
}
